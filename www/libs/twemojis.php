			if ($n >= self::$map[$i] && $n <= self::$map[$i+1]) {
				return '0x'.dechex($n);
			}
		}
		return false;
	}

	static function normalize($input) {
		foreach (self::$regexes as $old => $new) {
			$input = preg_replace($old, $new, $input);
